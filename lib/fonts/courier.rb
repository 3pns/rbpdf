TCPDFFontDescriptor.define('courier') do |font|
  # core font definition file for TCPDF (www.tcpdf.org)
  font[:type]='core'
  font[:dw]=600
  font[:cw] = {}
  0.upto(255) do |i|
  	font[:cw][i]=600
	end
end
